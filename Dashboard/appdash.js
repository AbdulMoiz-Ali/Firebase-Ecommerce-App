const hoverdash1 = document.getElementById("hov-sell1");
const plus = document.getElementById("plus");
const tit = document.getElementById("tit");
const im = document.getElementById("im");
const btncreate = document.getElementById("btncreate");
hoverdash1.addEventListener("mouseover", (e) => {
    plus.style.display = "flex";
    btncreate.style.display = "flex";
    document.getElementsByClassName("ack-drop-filter");
    tit.style.display = "none";
    im.style.display = "none";
})
hoverdash1.addEventListener("mouseleave", (e) => {
    plus.style.display = "none";
    btncreate.style.display = "none";
    tit.style.display = "flex";
    im.style.display = "flex";
});

